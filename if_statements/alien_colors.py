# This program will check what color an alien variable is and how many points
# it is worth, then it will send a message to the player telling them the info,

alien_color = 'blue'

if alien_color == 'green':
    print("The color is green.")
    print("You have gained 1 point.")
elif alien_color == 'red':
    print("The color is red.")
    print("You have gained 2 points.")
else:
    print("The color is blue.")
    print("You have gained 3 points.")
